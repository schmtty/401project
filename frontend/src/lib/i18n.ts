export function t(lang: Language, key: string, params?: Record<string, string>): string {
  const str = translations[lang]?.[key] ?? translations.en[key] ?? key;
  if (params) {
    return Object.entries(params).reduce((s, [k, v]) => s.replace(`{${k}}`, v), str);
  }
  return str;
}
